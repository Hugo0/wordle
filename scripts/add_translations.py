LANGUAGES = list(TRANSLATIONS.keys())


def add_translations():
    for lang in LANGUAGES:
        config_path = DATA_DIR / lang / "language_config.json"
        if not config_path.exists():
            print(f"WARNING: {config_path} does not exist, skipping")
            continue

        with open(config_path, "r", encoding="utf-8") as f:
            config = json.load(f)

        trans = TRANSLATIONS[lang]
        added = 0

        for section in ("text", "help", "ui"):
            if section not in trans:
                continue
            if section not in config:
                config[section] = {}
            for key, value in trans[section].items():
                if key not in config[section]:
                    config[section][key] = value
                    added += 1

        with open(config_path, "w", encoding="utf-8") as f:
            json.dump(config, f, ensure_ascii=False, indent=4)
            f.write("\n")

        print(f"{lang}: added {added} keys")

    # Verification: print ui.time_up for each language
    print("\n--- Verification: ui.time_up ---")
    for lang in LANGUAGES:
        config_path = DATA_DIR / lang / "language_config.json"
        if not config_path.exists():
            continue
        with open(config_path, "r", encoding="utf-8") as f:
            config = json.load(f)
        time_up = config.get("ui", {}).get("time_up", "MISSING")
        print(f"  {lang}: {time_up}")


if __name__ == "__main__":
    add_translations()
