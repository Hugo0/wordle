#!/usr/bin/env python3
"""Discover and rank semantic axes automatically.

Given a seed candidate pool of ~80 anchor-pair dimensions, score each one on
four criteria (AUC coherence, variance captured, anchor strength, PCA
alignment), then greedily select a final set of ~25 with orthogonality
rejection.

All scoring is automatic: no LLM calls are needed beyond fetching embeddings
for any anchor words not already in our cached vocab. Test words for AUC are
generated by k-NN bootstrap (the 15 nearest neighbours of each anchor in the
existing vocab).

Usage:
    uv run python -m scripts.semantic.find_axes
    uv run python -m scripts.semantic.find_axes --target-count 30 --orth 0.55
    uv run python -m scripts.semantic.find_axes --dry-run

Outputs:
    scripts/semantic/axis_report.md   — full ranked report (every candidate)
    scripts/semantic/axis_selected.json — the accepted set, ready for promotion

Does NOT modify production data/semantic/axes.json. Inspect the report,
decide which axes to promote manually.
"""

